package org.example.wsoc.model;

public class book {

    public int Id;
    public String Titulo;
    public String Autor;
    public String ISBN;
    public String AnioPublicacion;


    public int getId() {
        return Id;
    }

    public String getTitulo() {
        return Titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public String getISBN() {
        return ISBN;
    }

    public String getAnioPublicacion() {
        return AnioPublicacion;
    }

}

